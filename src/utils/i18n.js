export const getLocaleConfig = (language = 'en') => translations[language] || translations.en;

export const translateValue = (value, language = 'en') => {
  if (value && typeof value === 'object' && value[language]) return value[language];
  return value;
};

export const localizeCategory = (category, language = 'en') => {
  const translated = localizedCategories[category];
  return translated?.[language] || category;
};

export const localizeOptionValue = (options, value, language = 'en') => {
  const match = options.find((item) => item.value === value);
  return match ? translateValue(match.label, language) : value;
};

export const buildLocalizedCategoryOptions = (categories, language = 'en') =>
  categories.map((category) => ({
    value: category,
    label: localizeCategory(category, language),
  }));
